# Python can use '' or "" to describe a string 
print("Just'in")
print('Just"in')
print('c:\workspace')

# \t is a TAB in Python
print('c:\todo')
print('c:\\todo')

# prefix string 'r' to claim the following is Raw string
print(r'c:\todo')

# String is Immutable in Python
name = 'Justin'
print(len(name))
for ch in name:
    print(ch)
print('Just' in name)
print(name + name)
print(name*3)

